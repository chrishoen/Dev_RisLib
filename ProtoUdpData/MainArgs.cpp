#include "stdafx.h"

#define  _MAINARGS_CPP_
#include "MainArgs.h"

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Constructor.

MainArgs::MainArgs()
{
   reset();
}

void MainArgs::reset()
{
   mPeerIdent = 1;
}
   
void MainArgs::show()
{
   printf("MainArgs*************************\n");
   printf("PeerIdent       %d\n", mPeerIdent);
}

void MainArgs::help()
{
   printf("MainArgs\n");
   printf("-peer1    peer identifier is 1\n");
   printf("-peer2    peer identifier is 2\n");
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Parse the main program arguments into member variables.

void MainArgs::initialize(int argc, char** argv)
{
   reset();

   for (int i = 1; i < argc; i++)
   {
      //printf("argv[%d] %s\n", i, argv[i]);
      if (strcmp(argv[i], "-help") == 0) help();
      if (strcmp(argv[i], "-peer1") == 0) mPeerIdent = 1;
      if (strcmp(argv[i], "-peer2") == 0) mPeerIdent = 2;
   }
}

