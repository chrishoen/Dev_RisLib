// Execute a system command. return zero if successful.

int doSystemCommand(const char* aCommand, bool aPrintError)
{
   int tRet = system(aCommand);
   if (tRet && aPrintError)
   {
      printf("doSystemCommand FAIL1 %s\n",aCommand);
      return -1;
   }
   return 0;
}

