// Modify a gcode.

void Model::doModify(char* aString)
{
   Ris::CmdLineCmd* tCmd = new Ris::CmdLineCmd(aString);
   if (tCmd->isCmd("G1") && tCmd->gcodeIsValidZ())
   {
      Prn::print(0, "PROC G1 %.2f", tCmd->gcodeDoubleZ());
   }
   else
   {
      Prn::print(0, "IGNORE");
   }

}


